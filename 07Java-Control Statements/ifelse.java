public class ifelse {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println("a is greater than b");
        } else {
            System.out.println("a is not greater than b");
        }
    }
}

/*
if-else statement is a control flow statement that allows you to execute a block of code if a specified condition is true, and another block of code if the condition is false. In the above example, we compare the values of 'a' and 'b'. If 'a' is greater than 'b', it will print "a is greater than b". Otherwise, it will print "a is not greater than b".

The syntax of the if-else statement is as follows:
if (condition) 
{
    // code to be executed if condition is true
}
else {
    // code to be executed if condition is false
}

*/